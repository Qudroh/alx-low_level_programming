#include "3-calc.h"

/**
 * op_add - calc the sum of the int
 * @a: first int
 * @b: second int
 * Return: sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calc the diff of two ints
 * @a: first int
 * @b: second int
 * Return: differences of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calc the producr of two ints
 * @a: first int
 * @b: second int
 * Return: the product of a and b
*/
int op_mul(int a, int  b)
{
	return (a * b);
}

/**
 * op_div - calc the div of two ints
 * @a: first int
 * @b: second int
 * Return: division of a and b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc the remainder of the div of two ints
 * @a: first int
 * @b:second int
 * Return: remainder of a divided b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
